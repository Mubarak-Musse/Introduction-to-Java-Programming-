/**
 * the algorithm for calculating the area of circle can be described as follow 
 * step 1: Read in the circle's radius 
 * step 2: Compute the area using the following formula:
 * 	area = radius * radius * Pi ( Constant value )
 * Display result 
 *  
 * */


public class listing2_1 {
	public static void main(String[] args) {
		
		//Declare variable 
		
		double radius;
		double area;
		
		radius = 20;
		//compute area 
		area = radius * radius * 3.14159;
		
		//dispaly area 
		
		System.out.println("total:\" \n \t \\ " + area);
		System.out.println("\"Run!\" \n \"NO!\" ");
	}

}
